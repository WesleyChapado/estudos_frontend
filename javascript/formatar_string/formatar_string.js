var nome = "Wesley";
var sobrenome = "Chapado";
var idade = 29;

//adiciona variáveis dentro da string(place holder)
console.log(`O usuário ${nome} ${sobrenome} tem ${idade} anos`);//-> O usuário Wesley Chapado tem 29 anos

//conta a quantidade de caracteres da string
console.log(nome.length); //-> 6
console.log(sobrenome.length); //-> 7

//muda tudo para maiúsculo
console.log(nome.toUpperCase()); //-> WESLEY
console.log(sobrenome.toUpperCase()); //-> CHAPADO

//muda tudo para minúsculo
console.log(nome.toLowerCase()); //-> wesley
console.log(sobrenome.toLowerCase()); //-> chapado
